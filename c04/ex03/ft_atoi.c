int		ft_atoi(char *str)
{
	int bas;
	int sin;
	int i;

	bas = 0;
	sin = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sin = -sin;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		bas = bas * 10 + str[i] - '0';
		i++;
	}
	return (sin * bas);
}
